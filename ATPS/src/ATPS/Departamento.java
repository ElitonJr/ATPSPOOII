package ATPS;

public class Departamento {
	double Codigo;
	String Descrição;
	
	//getters e setters
	public double getCodigo() {
		return Codigo;
	}
	public void setCodigo(double codigo) {
		Codigo = codigo;
	}
	public String getDescrição() {
		return Descrição;
	}
	public void setDescrição(String descrição) {
		Descrição = descrição;
	}
	
}
